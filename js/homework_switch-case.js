let a = 'none';


if (a === 'block') {
    console.log('block')
} else if (a === 'none') {
    console.log('none')
} else if (a === 'inline') {
    console.log('inline')
} else {
    console.log('other')
}


  
switch (a) {
case 'block':
    console.log('block'); break;
case 'none':
    console.log('none'); break;
case 'inline':
    console.log('inline'); break;
default:
    console.log('other');    
}



